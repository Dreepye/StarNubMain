package utilities.datatypes.maybe;

public class Maybe<T> {

    private Is is;
    private T object;

    public Maybe() {
    }

    public Is getIs() {
        return is;
    }

    public void setIs(Is is) {
        this.is = is;
    }

    public T getObject() {
        return object;
    }

    public void setObject(T object) {
        this.object = object;
    }

    @Override
    public String toString() {
        return "Maybe{" +
                "is=" + is +
                ", object=" + object +
                '}';
    }
}
